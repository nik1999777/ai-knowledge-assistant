export const RAG_PROMPT_VERSION = "rag-grounded-v7";

export type RagAnswerMode = "strict" | "balanced" | "tutor";

export type RagPrompt = {
  system: string;
  prompt: string;
};

export function buildRagPrompt(
  question: string,
  contextChunks: string[],
  answerMode: RagAnswerMode = "balanced",
): RagPrompt {
  const context = contextChunks
    .map((chunk, index) => `Фрагмент ${index + 1}:\n${chunk}`)
    .join("\n\n");

  const system = buildSystemInstructions(answerMode);

  const prompt = `Документы:\n${context}\n\nВопрос: ${question}\n\nОтвет:`;

  return { system, prompt };
}

function buildSystemInstructions(answerMode: RagAnswerMode): string {
  const modePolicy = buildAnswerModePolicy(answerMode);

  return [
    "Ты — RAG-ассистент. Отвечай только на основе предоставленных документов.",
    "",
    "Правила:",
    "- Отвечай на русском языке.",
    "- Давай короткий, прямой ответ: 1–4 предложения или короткий список, если в контексте список.",
    "- Не пиши «возможно», «можно предположить», «вероятно» и подобные хеджирующие формулировки.",
    "- Если контекст содержит таблицу, список, заголовок или технические заметки — пересказывай их пункты.",
    "- Если режим предписывает отказ, выведи ровно и только фразу «Я не знаю на основе предоставленных данных.» — без пояснений.",
    "- Используй markdown там где уместно: ## заголовки, списки, **жирный**, `код`, блоки кода.",
    "",
    `Режим: ${answerMode.toUpperCase()}`,
    modePolicy,
  ].join("\n");
}

function buildAnswerModePolicy(answerMode: RagAnswerMode): string {
  switch (answerMode) {
    case "strict":
      return [
        "- Источник знаний: только предоставленный контекст.",
        "- Отвечай только если контекст явно содержит прямой ответ на вопрос.",
        "- Косвенные упоминания, частичное покрытие, логические выводы → отказ.",
        "- Внешние знания, общие пояснения и достройка терминов запрещены.",
      ].join("\n");
    case "balanced":
      return [
        "- Источник знаний: только предоставленный контекст.",
        "- Используй grounded partial answer: если контекст частичный, честно укажи что найдено и чего нет.",
        '- Если вопрос просит определить термин, а контекст только упоминает его: напиши "В найденных фрагментах X явно не определяется", затем кратко перечисли контекст употребления.',
        "- Внешние знания не используй.",
        "- Отказывайся только если контекст не содержит ничего релевантного.",
      ].join("\n");
    case "tutor":
      return [
        "- Источник знаний: контекст документов + ограниченные общие знания (только в блоке «Общее пояснение:»).",
        '- Блок "По документам:": только факты из предоставленного контекста.',
        '- Блок "Общее пояснение:": добавляй только если контекст хотя бы частично связан с вопросом.',
        "- Если контекст совсем не связан с вопросом — откажись без блока «Общее пояснение:».",
      ].join("\n");
  }
}
